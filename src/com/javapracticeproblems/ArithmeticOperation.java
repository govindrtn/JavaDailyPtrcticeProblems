package com.javapracticeproblems;

public class ArithmeticOperation {
	public static void main(String[] args) {
		
		////// Integer    
		System.out.println("Integer");
		
		int a = 7;
		int b = 6;
		
		int add = a + b ; //// Add
		int sub = a - b ; //// Sub
		int mul = a * b ; //// Mul
		int div = a / b;  //// div
		
		System.out.println("Addition---> " + add);
		System.out.println("Subtraction----> " + sub);
		System.out.println("Mul----> " + mul);
		System.out.println("Div ----> " + div);
		
//		Float
		System.out.println("Float");
		
		float a1 = 55.4F;
		float b1 = 55.1F;
		
		float add1 = a1 + b1 ; //// Add
		float sub1 = a1 - b1 ; //// Sub
		float mul1 = a1 * b1 ; //// Mul
		float div1 = a1 / b1 ; //// Div
		
		System.out.println("Addition---> " + add1);
		System.out.println("Subtraction----> " + sub1);
		System.out.println("Mul----> " + mul1);
		System.out.println("Div ----> " + div1);		
	}
}
